import { ApiCallLib } from '../../../libs'

// --------------------------------------------------------------------------------
// --------------------------------------------------------------------------------
// --------------------------------------------------------------------------------

// --------------------------------------------------------------------------------

function create(authenticationParams) {

  return {
    type: 'AUTHENTICATION_CREATE',
    promise: ApiCallLib.post('/signin', {
      authentication: authenticationParams.credentials
    })
  }
} // <= create

// --------------------------------------------------------------------------------

function destroy() {
  return {
    type: 'AUTHENTICATION_DESTROY'
  }
} // <= destroy

// --------------------------------------------------------------------------------

function init() {
  return {
    type: 'AUTHENTICATION_INIT'
  }
}

// --------------------------------------------------------------------------------

function reset() {
  return {
    type: 'AUTHENTICATION_FAILURE_RESET'
  }
}

// --------------------------------------------------------------------------------
// --------------------------------------------------------------------------------
// --------------------------------------------------------------------------------


export default {
  create,
  destroy,
  init,
  reset
};
