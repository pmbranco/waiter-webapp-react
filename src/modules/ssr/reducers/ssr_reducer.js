function ssrReducer(state = [], action) {
  if (!process.browser && (action.type).endsWith('_REQUEST')) {
    return _setPromises(state, action.promise);
  }
  else {
    return state;
  }
} // <= ssrReducer

// --------------------------------------------------------------------------------
// --------------------------------------------------------------------------------
// --------------------------------------------------------------------------------

function _setPromises(state, promise) {
  if (!promise) {
    return state;
  }
  return state.concat(promise);
}

// --------------------------------------------------------------------------------
// --------------------------------------------------------------------------------
// --------------------------------------------------------------------------------

export default ssrReducer;
